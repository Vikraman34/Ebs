    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        tempUser temp=new tempUser();
        temp.name=u.getText();
        temp.num=Integer.parseInt(n.getText());
        
         ArrayList<UserDetails> B;
        
        try{
        FileInputStream fi1=new FileInputStream("User.tmp");
        ObjectInputStream o1 =new ObjectInputStream(fi1);
        B=(ArrayList<UserDetails>) o1.readObject();

        o1.close();
        fi1.close();
        }
        catch(Exception e)
        {
                B=new ArrayList<UserDetails>();
        }
        UserDetails t=new UserDetails();
        int flag=0;
        for(int i=0;i<B.size();i++)
        {
            t=B.get(i);
            if(t.getName().equals(temp.name) && (t.getCard_num()==temp.num))
            {
                flag=1;
                temp.index=i;
                JOptionPane.showMessageDialog(this,"User Found");
                break;
            }
            else
                flag=0;
        }
       
       
       if(flag==1)
       {
       B.remove(temp.index);
       
       try{
            FileOutputStream fi=new FileOutputStream("User.tmp");
            ObjectOutputStream o =new ObjectOutputStream(fi);

            o.writeObject(B);

            o.close();
            fi.close();
            }
            catch(Exception e)
            {
                
            }
        JOptionPane.showMessageDialog(this,"User delted successfully");
       }
       
         if(flag==0)
        {
            JOptionPane.showMessageDialog(this,"No User Found");
        }
        
        Page2 ob=new Page2();
        this.dispose();
        ob.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

